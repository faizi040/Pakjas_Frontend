            <h2 class="mt-3"></h2>
            <h3>Publication ethics</h3>
            <p class="just-para">High quality scientific publications and public trust in scientific information require ethical standards to be followed by the authors as well as by the editorial team members. It also includes that all the contributors receive credits as per their work in the publication.</p>
            <h3>Article assessment</h3>
            <p class="just-para">Article assessment is based on editors’ observations and reviewers’ comments on the submitted article. Articles are only considered for peer review if it satisfies the editors and fulfill the basic requirement of journal publication policy. It decreases the burden on reviewers who do this job voluntarily and will improve the efficiency of reviewing process. The dynamic editorial team work very hard to select high quality manuscripts for review process. However, the identity of reviewers remains anonymous whether the manuscript is accepted or rejected even after the accomplishment of the editorial process. Article assessment is transparently based on editor’s observation and reviewers detailed comments about the article.</p>
            <h3>Plagiarism</h3>
            <p class="just-para">Authors are strictly forbidden to use the text, figures, and/or ideas of others without due acknowledgement. All the related information must be cited properly using the original source of information. It is unethical to cite any article without reading that article objectively. The reuse of others text can be quoted, however is limited, and mentioned accordingly. Any manuscript with more than 19% text similarity are considered as plagiarized and editor has right not to proceed without giving any reason. In case of detection of plagiarized data, authors may face sanctions. Furthermore, article may need rectification or retraction, if it has been published already.</p>
            <h3>Duplicate submission and redundant publication</h3>
            <p class="just-para">Pakistan Journal of Agricultural Sciences accept only original content. It means that the articles which are previously published, including in a language other than English cannot be submitted in the journal for publication. Manuscripts submitted to PAKJAS journal must not be submitted elsewhere while under review and must be withdrawn before being submission to any other journal. Authors are responsible to take care of it and articles found submitted more than one journals simultaneously may suffer sanctions.

If authors are submitting manuscript similar to their own already published articles, they must cite that article in the manuscript and must describe the difference with previously published one. Authors cannot use their own figures or substantial amounts of wording from published article, figure may be reproduced after getting permission from the copyright holders’ authors and/or publishers. Duplicate publication of same data published in the article may result in the retraction of duplicated article (published later) and the authors may experience sanctions.</p>
            <h3>Citation manipulation</h3>
            <p class="just-para">Authors should cite the articles as per requirement of the manuscripts and not to increase the citations only for any non-scientific reason. Furthermore, editors and reviewers should also not ask to cite specific articles to enhance the citation score of their own or any specific journal, however needed and relevant studies may be asked to refer to improve the discussion.</p>
            <h3>Fabrication and falsification</h3>
            <p class="just-para">Fabricated or falsified data or results is a professional crime and any article found having such data will not be accepted and will be retracted any time, if published. Furthermore, authors may experience sanctions.</p>
            <h3>Authorship and acknowledgements</h3>
            <p class="just-para">Only those authors should be/must included who have significantly contributed the research presented in the manuscript and agreed to be an author. Authors’ contributions need to be described at the time of manuscript submission. Changes in authorship can be made with the written consent of all other authors before the acceptance of the article and will not be entertained after acceptance.

Those who have contributed to the research in anyway and are not authors in the manuscript may be accredited in the acknowledgement with their permission.

Only one of the authors can submit the article and preferably submitting author should be corresponding author in the manuscript.</p>
            <h3>Conflicts of interest</h3>
            <p class="just-para">Conflicts of interest (COI) occur when matters outside research may significantly affect the impartiality of the work or its evaluation. Mentioning conflict of interest does not restrict the process of reviewing and publishing, rather declaration of any possible conflicts of interest will support to bring up high quality research. It would be very uncomfortable and embarrassing for authors, editors, and journal if any COI appears after publication and may lead to publish corrigendum, reassessment of manuscript or retraction from the journal.

Conflicts of interest may be related to financial, affiliations, intellectual property, ideological, personal, and academic.

Submitting author is responsible for coauthors declaring any conflict of interests regarding the publication of paper, if any. Editors and reviewers can only take care of declared conflicts of interest.

Editors and reviewers must not involve themselves if they have combined recent publications (during last two years), close collaboration, share the same professional affiliation, close personal relations, blood relations and any financial interests related to outcome of the work. However, if a reviewer thinks that he/she can assess the work without any biasness in the presence of any above-mentioned conflict, he/she must discuss with editor.</p>

            





