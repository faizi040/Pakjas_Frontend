            <h3 class="mt-3">Reviewers Guidelines</h3>
            <p class="just-para">Peer review process is the base of good science and it is an obligation of a reviewer to spare some time for peer review for the sake of science and development. Effective peer review is key to the success, and the journal team work very hard to establish an environment for a reviewer for an effective review. Reviewers are requested to review the article, however they have right not to review the article without giving any reason. However, we expect that if the invited reviewer is not available due to any reason, he/she may propose two relevant reviewers for that manuscript. There are following features of a reviewing process:
                <br><br>
            <ol type="1">
                <li>As per journal policy the review is single blind which means that the identity of author is disclosed to an editor, however identity of reviewer is not disclosed to author.</li>
                <li>Reviewer should not be relative of the author/s or very close collaborator or from the author/s’s institute. If such reviewers are invited, they should deny reviewing. However, if he/she can perform review without any biasness, he may discuss with editor.</li>
                <li>We expect that a reviewer has read the ethical guidelines for peer reviews at https://doi.org/10.24318/cope.2019.1.9</li>
                <li>The reviewer should assess the manuscripts without considering the nature of the authors, race, ethnic origin, religion, and institution in he/she works.</li>
                <li>The reviewer should/must not disclose the manuscript to anyone without permission of editor</li>
                <li>The manuscript material which is not published yet must not be used by the reviewers</li>
                <li>To acknowledge the efforts and commitment of reviewer, journal has policy to pay honorarium of 1500 pkr to national reviewers against each in-time review as a token of thanks. In time review mean review submitted in two weeks. For international reviewers journal waive of article processing charges (APC) of manuscript against every 10 in-time reviews.</li>
            </ol>

            </p>
            <h3 class="mt-3">How to peer review for PAKJAS?</h3>
            <p class="just-para">
                Reviewers are provided with an assessment sheet to answer the questions related to manuscript quality; however, reviewers’ comments are must to consider it a complete review. Therefore, reviewers are requested to add at least few comments in the given comment box or add separate sheet for comments to author. It is suggested to categorize comments in three parts: <br><br>
            <ul>
                <li>Overall comment</li>
                <li>Major Issues</li>
                <li>Minor Issues</li>
            </ul>
            There is an option to provide confidential comments to editor as well, however not obligatory
            </p>
            <h3 class="mt-3">Questions asked in manuscript assessment sheet (Yes/No)</h3>
            <p class="just-para">
            <ol type="1">
                <li>The subject and contents of the manuscript are suitable for publication in PJAS</li>
                <li>Title is suitable and represent the contents of the paper</li>
                <li>The abstract is appropriate and provide sufficient information</li>
                <li>Novelty of the results is appropriate</li>
                <li>Methods and experimental design are appropriate</li>
                <li>SI units are used correctly for the data reported</li>
                <li>Size of the paper is appropriate with respect to the scientific content
                </li>
                <li>The number of tables and figures is appropriate</li>
                <li>The linguistic quality of the paper is good for publication</li>
                <li>Conclusions are well proven</li>
                <li>Number and quality of references is appropriate</li>
                <li>Limitations of research has been acknowledged</li>
            </ol>
            Reviewers are encouraged to give comments for authors criticizing the methods, results and conclusions drawn. Finally, reviewers are asked to select the suitable category from the following:
                <ul >
                    <li>Accept in present form</li>
                    <li>Consider after Minor Revision</li>
                    <li>Reassess after Major Revisions</li>
                    <li>Reject</li>
                </ul>
                However, it is important to note that the overall decision will be made by the subject editor. If any of the reviewer give answers of 6 questions as no, editor has right to reject the paper without giving any further reason.
            </p>







