            <p class="just-para">Peer review process is the base of good science and rigorous review
                of any submitted manuscript identify high quality science to be presented to scientific
                community. <strong>Pakistan Journal of Agricultural Sciences</strong> has transparent and unbiased peer
                review process where a manuscript pass through the hands of at least five researchers
                belongs to existing scientific community from submission to publishing. The complete
                editorial work is accomplished using semi-online review system. The submitted manuscripts
                for publication, editorial office check it to ensure the meta data and manuscript files.
                Afterwards manuscript is sent to managing editor for peer review process. It is three
                phase peer
                review system to identify high quality research to be published in the journal. <br><br>
                <strong>Phase I.</strong> Managing editor screen the newly submitted manuscripts in the light of similarity index, linguistic quality, authenticity of data, presentation of data and format of the journal.
                Screened manuscripts are sent to subject editors for further evaluation. <br><br>
                <strong>Phase II.</strong> Subject editors examine the science and determine the novelty of the data and contribution to already existing scientific knowledge. Subject editor either propose rejection at this stage
                or send it to reviewers for further evaluation from two neutral subject experts. <br><br>
                <strong>Phase III.</strong> Two independent reviewers evaluate the manuscripts based on provided guidelines and suggest one of the following recommendations along with his/her comments <br><br>

            <ol type="1">
                <li>Accept in present form</li>
                <li>Consider after minor revision</li>
                <li>Reassess after major revision</li>
                <li>Reject</li>
            </ol><br>
            Independent reviewers submit their recommendations to subject editors and in the light
            of independent reviewersâ€™ comments subject editor suggests first or fourth of the above
            four options to managing editor along with own comments. However, if editor want to get revisions, the manuscript is sent to authors. Upon submission of revised manuscript subject editor
            suggest option 1 or 4 of the above recommendations along with his/her own comments.


