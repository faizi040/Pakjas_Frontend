            <h4 class="mt-3">
            Statistical Note for Authors
            </h4>
            <p class="just-para">
            The Pakistan Journal of Agricultural Sciences has a tradition of setting high standards regarding the statistical methods contained in its papers. Although it is impracticable to present here a comprehensive survey of acceptable statistical analyses, it is nevertheless useful to point out some common practices which have and have not found favor with the editors. To speed up assessment of submitted papers, authors are advised to pay particular attention to the following. <br><br>
            <ol type="1">
                <li>The description of the experimental designs and statistical analyses should be clear and concise. From this description, readers must be able to understand exactly how the experiment was conducted and how the data were analyzed. When presenting initial numerical summaries of the experimental material (e.g., starting weights, ages) variation should be represented by ranges or standard deviations.</li>
                <li>The favored method of presenting experimental results is by quoting estimated values of the relevant statistics (mean values, regression coefficients, etc.), together with the appropriate standard errors of those estimates. The degrees of freedom (D.F.) on which the standard errors (S.E.) are based should also be quoted. This will usually assist the referees and the general reader in understanding the experimental procedure.</li>
                <li>Each statistical method has its own assumptions and should be used only if all its requirements are fulfilled. Analysis of Variance (ANOVA) requires that the response variable should be normally distributed and uncorrelated with equal variance at each level of the qualitative factor.</li>
                <li>Authors should make every effort to ensure that the standard errors which are quoted are suitable for the comparisons which they wish to make. When in doubt, authors should seek the guidance of a statistician.</li>
                <li>Repeated measurements over time or spatial data from, for example, crop disease or competition studies often give rise to correlated data that require special methods of analysis. Usually, it will be necessary to seek specialist advice before attempting an analysis of data of this type. A standard reference book is Diggle, P.J., K-Y. Liang, and S.L. Zeger. 1994. The Analysis of Longitudinal Data. Oxford: Oxford University Press.</li>
                <li>The Journal will not publish tables containing a proliferation of asterisks or other indicators of statistical significance. Although statistically appropriate tests of hypotheses are acceptable, they should be employed sparingly and with discretion. Probability values (e.g. P less than0.01) may be quoted in the text.</li>
                <li>Standard statistical models should be fully described using correct terminology so that the reader can understand the techniques that were used to model the data. Normally, this will involve some discussion of the data and some explanation of the choice of statistical model used.</li>
                <li>The uncritical and indiscriminate use of 'multiple comparison' procedures, particularly when the treatment structure provides a logical basis for testing, is inappropriate. Since the researchers always strive for planned comparisons, therefore, whenever the treatments have intrinsic structure only the contrast analysis address such research hypotheses. Whenever a researcher uses a quantitative treatment, the objective is to find the level of the quantitative treatment which gives the optimum value of the response and this can be achieved using trend analysis. The results of exhaustive, retrospective tests of hypotheses are not acceptable.</li>
                <li>Authors should aim to combine the virtues of simplicity and statistical rigour in the analysis of their data. Unnecessarily complex statistical methodology should be avoided. Where more sophisticated procedures are essential, great care needs to be taken in describing the method, and adequate references should be cited.</li>
                <li>The Journal will not normally publish routine Analysis of Variance tables used for calculating standard errors and significance tests. The underlying Analysis of Variance tables should be shown only if components of variance are of especial interest or if an unavoidably complex design has been used.</li>
                <li>Where a statistical package is used for analysis or modeling of data, it will normally be necessary to give an explicit reference to the package and the techniques used with appropriate page numbers from the Reference Manual. With editorial agreement, novel computer code may be listed in an appendix.</li>
                <li>Statistical models with factorial structure must normally conform to the principle that factorial interaction effects of a given order should not be included unless all lower order effects and main effects contained within those interaction effects are also included. Similarly, models with polynomial factor effects of a given degree should normally include all corresponding polynomial factor effects of a lower degree (e.g. a factor with a quadratic effect should also have a linear effect).</li>
                <li>Main effects should be explained/ exploited only if interaction involving them is not significant. Otherwise, the significant interaction should be explored further, and focus should be on the interaction effects only.</li>
            </ol>
            </p>
