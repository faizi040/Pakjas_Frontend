            <h4 class="mt-5">Authors Guideliness</h4>

            <p class="just-para">
                The Journal accepts electronic submissions using website login only.
                Full instructions are available on the website at https://www.pakjas.com.pk and by
                clicking <span class="text-success">“Sample Paper”</span> of “PAKJAS” can be accessed for precise formatting”.
                The Editorial Office is pleased to offer help and advice to potential authors.
                For further queries not related to formatting can
                be asked through the E-mail: pakjas@uaf.edu.pk <br><br>
                The journal welcomes concise papers presenting original research data or methodology
                from authors throughout the world.
                The editors wish to continue the policy of the Journal, since its foundation in 1964,
                for publishing papers in all aspects of agricultural sciences. Plant and crop science,
                soil science, animal science, environmental science, and the relationships between them
                are covered. The editors also welcome papers relating new scientific technology or concepts in the field of genetics, biochemistry, biophysics and molecular biology to agricultural practice. The journal has cushion to publish very limited number of high-quality manuscripts related to agricultural economics and agricultural impact on society and vice versa. In addition, the Journal publishes critical reviews from time to time, normally by invitation, on topics of interest to its readership. <br><br>
                The Editors must be informed if any of the material submitted has been published elsewhere. If a paper is accepted, it must not be published elsewhere in the same form. Experiments on animals must conform to the legislation in the country where the experiments were carried out. Work based on limited experimentation will not generally be considered acceptable. Work of complete local interest only is not considered appropriate for this journal.
            </p>
            <h4 class="mt-3">Scripts/submission of papers:</h4>
            <p class="just-para">Papers, written in English, must be submitted for review at <span class="text-success">https://www.pakjas.com.pk.</span> The preferred word processing packages are Word, and the preferred graphics package is TIFF, JPEG. Please indicate the file format used. <st<strong>rong>Authors are requested to submit manuscript without authors name and affiliation and must provide the authors’ name along with affiliation while submitting the manuscripts in metadata.</strong></p>

            <h4 class="mt-3">Layout and style:</h4>
            <p class="just-para">The journal’s system of refereeing is single blind and protect the identity of reviewers. The full name and address of the institution where the research was done should be stated. Change of address may be given as a footnote. Indicate the name and address of corresponding author with “*” to which proofs should be sent and the e-mail address. <br>

                <strong>Font type:</strong> Times New Roman <br>
                <strong>Font size:</strong> 12 <br>
                <strong>Line space:</strong> 1.5 <br>
                <strong>Line Numbers:</strong> Continuous line numbers from start of article to end of the document is compulsory <br>
            </p>
            <h4 class="mt-3">Title page:</h4>
            <p class="just-para">
                The title should be informative but concise and should not contain abbreviations. Title should be written in title case. It should name the organism studied, where relevant. A short title, not exceeding 50 characters, should be provided for the running headlines.
            </p>
            <h4 class="mt-3">Abstract:</h4>
            <p class="just-para">
                placed at the beginning of the text, should briefly indicate the <strong>background and purpose, methodology, results, and conclusion</strong> without subheadings in the sequence mentioned in this sentence. Failing to develop such abstract will lead to rejection without review.
            </p>
            <h4 class="mt-3">Main Text:</h4>
            <p class="just-para">
                It is usually convenient to divide the paper into 4 main sections, viz. Introduction, Materials and Methods, Results, Discussion and References in the end. Combined Results and Discussion are not acceptable. Too many subheadings should be avoided. Before the references ‘conflict of interest’ and authors’ contribution statement must be declared. <br>
                The Introduction should set the work in context, present only essential background, and include a concise statement of the objectives; a detailed review of the literature is not necessary. Relevant details should be given of the experimental materials and design, and the techniques and statistical methods used. Statistical guidelines are available on request. Numerical results should be shown in the tables and should not be repeated in the text. SI units should be used exclusively e.g. kg ha-1, mg L-1. Be sure that abbreviation of liter is capital ‘L’ even when used as ‘mL’. Use of % should be restricted and used only to describe relative changes in responses. Experimental details and results should be reported in the past tense. The discussion should draw together the results, briefly relate the author's results to other work on the subject, summarize any implications and applications and give the author's conclusions. Results repetition in discussion section will lead towards negative outcome of the submission. Footnotes should not be used. All abbreviations used should be fully explained at first mention. No sentence should not be started with abbreviation.
            </p>
            <h4 class="mt-3">Tables:</h4>
            <p class="just-para">
                must be numbered consecutively in the order in which they are cited in the text. Numerical results should be displayed as means with their relevant standard errors. Least significant difference (LSD) values should be mentioned in the table or in the food note and alphabets should be used with means to compare ethe significant differences. Normally a mean should be rounded to one-tenth of its standard error and the standard error given to one place of decimals more than the mean. The title should fully describe the contents of the table and explain any abbreviations used in it. The Journal prefers not to have a proliferation of asterisks, superscript letters, etc. in tables. Asterisks should not be used to denote statistical significance. However, if footnotes are necessary, the following symbols may be used in this order: * † ‡ §.
            </p>
            <h4 class="mt-3">Figures:</h4>
            <p>should be restricted to the display of results where many values are presented, and interpretation would be more difficult in a table. Figures may not reproduce the same data presented in tables and must be cited in the text. Figures should be embedded (in pic format) in the text at appropriate position, however separate files must be provided on demand of editorial office for high quality figures production. All files to be clearly named. The legend of every figure should be in detail enough to make the figure self-explanatory. The legend should explain the parameters and treatments, and statistical analysis used. LSD value must be provided where needed. Failing to provide such figures will not be proceeded for publication.</p>

            <h4 class="mt-3">
                References:
            </h4>
            <p class="just-para">
                In the text, a reference should be quoted by the author's name and date in parentheses, in date order, e.g. (Jarvis, 1994; Edmondson, 1998). Where there are three or more authors, the first name followed by et al. should be used. A list of references should be given at the end of the text listing, in alphabetical order as per following examples:
            </p>

