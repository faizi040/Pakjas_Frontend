            <h4 class="mt-3">
                Style and Editorial Conventions:
            </h4>
            <p class="just-para">
                Only SI units will be accepted in the manuscript. <br>
                Give cation exchange capacity in mmol (+)/kg not meq. Please use g kg-1, mg kg-1, mg L-1, μM, mL L-1, dS m-1 etc. <br>
                Use kg ha-1, or t ha-1, not quintals. <br>
                Use 'litres' in full after numbers where confusion could arise with the numeral L; e.g. use 5 litres/day but 5 mL L-1. <br>
                Rates should be expressed by a solidus, e.g. kg ha-1, 6 kg ha-1, 3 plants m-² (not 3 plants m-²), 7 kg ha-1 per year. <br>
                Do not repeat units in lists, e.g. 3, 10, 17 and 30°C; 20 or 30% more with no space. Use % after numbers, not per cent, e.g. 7%. <br>
            </p>
